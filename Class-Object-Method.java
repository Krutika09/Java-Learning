// This is a CLASS
class Student {

    // This is a METHOD
    void read() {
        System.out.println("Student is reading");
    }
}

public class Main {
    public static void main(String[] args) {

        // This is an OBJECT of Student class
        Student s1 = new Student();

        // Calling METHOD using OBJECT
        s1.read();
    }
}
